#Soma, Subtração, Multiplicação e Divisão de dois números

n1 = float (input('Informe o primeiro número para realizar as operações: '))
n2 = float (input('Informe o segundo número para realizar as operações: '))

soma = n1 + n2
subtracao = n1 - n2
divisao = n1/n2
mult = n1*n2

print ('Soma: ', soma, ', Subtração: ', subtracao, ', Divisão: ', divisao, ', Multiplicação: ', mult,)